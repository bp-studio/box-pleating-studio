<template>
	<div class="d-flex">
		<div class="flex-grow-1">
			<i :class="icon"></i>
			<slot></slot>
		</div>
		<div class="ms-3 text-end desktop-only">{{key}}</div>
	</div>
</template>

<script lang="ts">
	import { Component, Prop, Vue } from 'vue-property-decorator';

	@Component
	export default class HotKey extends Vue {
		@Prop(String) public icon: string;
		@Prop(String) public hk: string;
		@Prop(Boolean) public ctrl: boolean;
		@Prop(Boolean) public shift: boolean;

		protected get isMac(): boolean { return isMac; }

		protected get key(): string {
			let result = this.hk;
			if(this.shift) result = (isMac ? '⇧' : 'Shift+') + result;
			if(this.ctrl) result = (isMac ? '⌘' : 'Ctrl+') + result;
			return result;
		}
	}
</script>
